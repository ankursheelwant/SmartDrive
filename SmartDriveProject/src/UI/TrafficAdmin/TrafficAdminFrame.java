    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)workRequestJTable.getModel();
        
        model.setRowCount(0);
        
        for(WorkRequest request : enterprise.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[6];
            row[0] = request;
            row[1] = request.getSender().getPerson().getName();
            row[2] = request.getReceiver() == null ? null : request.getReceiver().getPerson().getName();
            row[3] = request.getStatus();
            if(request instanceof RequestAmbulance)
            {
                row[4] = ((RequestAmbulance)request).getLocation();
                if(((RequestAmbulance)request).isIsEnroute())
                    row[5] = "Yes";
                else
                    row[5] = "No";
            }
            if(request instanceof TrafficCongestionAlert)
            {
                row[4] = ((TrafficCongestionAlert)request).getLocation();
                row[5] = "";
            }
            model.addRow(row);
        }
    }
    
