    private void processTrafficAlertBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processTrafficAlertBtnActionPerformed
        // TODO add your handling code here:
        if(!locationTextfield.getText().trim().isEmpty()&&validator.isValidAlphaNumeric(locationTextfield.getText().trim()))
        {
            TrafficCongestionAlert trafficAlert=new TrafficCongestionAlert();
            trafficAlert.setSender(account);
            trafficAlert.setRequestDate(new Date());
            trafficAlert.setMessage("Traffic Congestion");
            trafficAlert.setLocation(locationTextfield.getText().trim());
            for(Enterprise ent:network.getEnterpriseDirectory().getEnterpriseList())
            {
                for(Organization org:ent.getOrganizationDirectory().getOrganizationList())
                {
                    if(org instanceof CitizenOrganization)
                    {
                        org.getWorkQueue().getWorkRequestList().add(trafficAlert);
                        for(UserAccount ua:org.getUserAccountDirectory().getUserAccountList())
                        {
                            ua.getWorkQueue().getWorkRequestList().add(trafficAlert);
                        }
                    }
                }
            }
            enterprise.getWorkQueue().getWorkRequestList().add(trafficAlert);
        }
        else
        {
            locationTextfield.setBackground(Color.pink);
            warningLabel1.setText("Invalid address input");
        }
        populateTable();
        
    }//GEN-LAST:event_processTrafficAlertBtnActionPerformed

    private void processAmbulanceAlertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processAmbulanceAlertActionPerformed
        // TODO add your handling code here:
        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            warningLabel.setText("Select a row first!!");
            return;
        }

        if(workRequestJTable.getValueAt(selectedRow, 0) instanceof RequestAmbulance)
        {
            RequestAmbulance request = (RequestAmbulance)workRequestJTable.getValueAt(selectedRow, 0);
            request.setIsEnroute(true);
            for(Enterprise ent:network.getEnterpriseDirectory().getEnterpriseList())
            {
                for(Organization org:ent.getOrganizationDirectory().getOrganizationList())
                {
                    if(org instanceof CitizenOrganization)
                    {
                        org.getWorkQueue().getWorkRequestList().add(request);
                        for(UserAccount ua:org.getUserAccountDirectory().getUserAccountList())
                        {
                            if(!ua.equals(request.getSender()))
                            {
                                ua.getWorkQueue().getWorkRequestList().add(request);
                            }
                        }
                    }
                }
            }
        }
               
        populateTable();
        
    }//GEN-LAST:event_processAmbulanceAlertActionPerformed

